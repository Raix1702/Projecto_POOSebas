

package Registrar_Movimientos_Caja_Chica;


public class RegistroMovimiento{
    private String cmov,fmov,tmov;
    private int mmov;

    public RegistroMovimiento() {
    }

    public String getCmov() {
        return cmov;
    }

    public void setCmov(String cmov) {
        this.cmov = cmov;
    }

    public String getFmov() {
        return fmov;
    }

    public void setFmov(String fmov) {
        this.fmov = fmov;
    }

    public String getTmov() {
        return tmov;
    }

    public void setTmov(String tmov) {
        this.tmov = tmov;
    }

    public int getMmov() {
        return mmov;
    }

    public void setMmov(int mmov) {
        this.mmov = mmov;
    }
    
    
    
    
    
}